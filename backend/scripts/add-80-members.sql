    membership_level,
    COUNT(*) as تعداد
FROM members
WHERE member_type = 'ورزشکار'
GROUP BY membership_level
ORDER BY 
    CASE membership_level
        WHEN 'پلاتینیوم' THEN 1
        WHEN 'طلایی' THEN 2
        WHEN 'نقره‌ای' THEN 3
        WHEN 'برنزی' THEN 4
    END;

