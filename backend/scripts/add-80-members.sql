    gender,
    COUNT(*) as تعداد,
    COUNT(CASE WHEN subscription_status = 'فعال' THEN 1 END) as فعال,
    COUNT(CASE WHEN subscription_status = 'غیرفعال' THEN 1 END) as غیرفعال
FROM members
WHERE member_type = 'ورزشکار'
GROUP BY gender
ORDER BY gender;

-- نمایش توزیع سطح عضویت
